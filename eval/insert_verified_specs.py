#!/usr/bin/env python3
# ruff: noqa: E402

"""Script to insert the first successfully-verified spec for each function into a C source file."""

import argparse
import json
import sys
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))

from loguru import logger

from util.function_util import (
    CFunctionGraph,
    FunctionSpecification,
    get_source_content_with_specifications,
)


def main() -> None:
    """Insert verified specs into a C source file."""
    parser = argparse.ArgumentParser(
        description="Insert the first successfully-verified spec for each function into a C file."
    )
    parser.add_argument("c_src", type=Path, help="Path to the C source file.")
    parser.add_argument(
        "verification_summary",
        type=Path,
        help="Path to the verification summary JSON generated by 'get_verification_summary.py'",
    )
    parser.add_argument(
        "-o",
        "--output",
        type=Path,
        default=None,
        help="Output file path. Defaults to stdout.",
        dest="output_path",
    )
    args = parser.parse_args()

    summary = json.loads(args.verification_summary.read_text(encoding="utf-8"))
    function_graph = CFunctionGraph(args.c_src)

    successfully_verified_functions = {}
    for specs_for_function in summary.get("functions", []):
        verifying_specs = specs_for_function.get("verifying_specs", [])
        if not verifying_specs:
            continue
        function_name = specs_for_function["function_name"]
        if function := function_graph.get_function_or_none(function_name=function_name):
            first_spec = verifying_specs[0]["spec"]
            specification = FunctionSpecification(
                preconditions=first_spec["preconditions"],
                postconditions=first_spec["postconditions"],
            )
            successfully_verified_functions[function] = specification
        else:
            logger.warning(f"Function '{function_name}' missing from {args.c_src}")

    if not successfully_verified_functions:
        logger.warning(f"No successfully-verified specifications for '{args.c_src}'")
        sys.exit(1)

    result = get_source_content_with_specifications(successfully_verified_functions, function_graph)

    if args.output_path:
        args.output_path.write_text(result, encoding="utf-8")
    else:
        sys.stdout.write(result)


if __name__ == "__main__":
    main()
