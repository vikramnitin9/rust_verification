"""Classes for working with Avocado stubs."""

from __future__ import annotations

import pickle as pkl
import re
from dataclasses import dataclass
from pathlib import Path
from types import MappingProxyType
from typing import cast

AVOCADO_FUNCTION_PREFIX = "_avocado_"
AVOCADO_STUB_DIR = "verification/cbmc_stubs"


@dataclass(frozen=True)
class RenameData:
    """Data for renaming functions in libraries defined in the ANSI-C standard to Avocado stubs.

    The CBMC verifier includes its own copy of stub functions in libraries defined in the ANSI-C
    standard that it uses for verifying client code that makes use them. This can lead to
    issues related to internal C functions that are used by its copy of stub functions:

        https://github.com/diffblue/cbmc/issues/8844

    Avocado stubs aim to mitigate this issue by ensuring only the functions directly in the copy of
    the standard library are used. However, standard library functions cannot be defined twice,
    and so each function (i.e., stub) is prepended with `avocado_` to avoid naming collisions.


    See `generate_avocado_stubs.py` for how Avocado stubs are generated.

    Attributes:
        renamed_identifier (str): The function's name as an Avocado stub.
        file_path (Path): The path to the CBMC stub file in which the function is defined.
    """

    renamed_identifier: str
    file_path: Path


class AvocadoIdentifierRenamer:
    """Class for applying renaming (i.e., ANSI-C identifier -> Avocado identifiers) in C files.

    Attributes:
        _ansi_id_to_rename_data (dict[str, RenameData]): Map from ANSI-C library function
            identifier(s) to their renamed Avocado identifiers. See: `RenameData` class above.
    """

    _ansi_id_to_rename_data: dict[str, RenameData]

    # This is generated by `generate_avocado_stubs.py`
    DEFAULT_STUB_MAPPINGS = f"{AVOCADO_STUB_DIR}/c_stub_rename_map.pkl"

    def __init__(self, path_to_stub_mappings: Path = Path(DEFAULT_STUB_MAPPINGS)) -> None:
        """Create a new AvocadoIdentifierRenamer."""
        try:
            data = pkl.load(path_to_stub_mappings.open(mode="rb"))
            self._ansi_id_to_rename_data = cast("dict[str, RenameData]", data)
        except Exception as e:
            msg = f"Failed to load Avocado stub mappings from: {path_to_stub_mappings}"
            raise ValueError(msg) from e

    def rename_ansi_identifiers_to_avocado_identifiers(self, file_content: str) -> str:
        """Return the file content where ANSI library function ids are replaced with Avocado ids.

        Args:
            file_content (str): The content of the file to which to apply renaming.

        Returns:
            str: The file content after ANSI library functions have been renamed.
        """
        file_content_with_renaming = file_content
        for original_identifier, rename_metadata in self._ansi_id_to_rename_data.items():
            # Don't replace identifiers twice (i.e., replace only exact matches)
            name_to_replace_pattern = r"\b" + original_identifier + r"\b"
            file_content_with_renaming = re.sub(
                name_to_replace_pattern,
                rename_metadata.renamed_identifier,
                file_content_with_renaming,
            )
        return file_content_with_renaming

    def get_identifier_mapping(self) -> MappingProxyType[str, RenameData]:
        """Return the mapping from ANSI-C identifiers to Avocado identifiers.

        Returns:
            MappingProxyType[str, RenameData]: The mapping from ANSI-C identifiers to Avocado
                identifiers.
        """
        return MappingProxyType(self._ansi_id_to_rename_data)
